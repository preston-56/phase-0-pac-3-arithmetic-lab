// basic math functions

function add(a, b) {
    return a + b;
 
}

function subtract(a, b) {
    return a - b;
    
}

function multiply(a, b) {
    return a * b;

}

function divide(a, b) {
    return a / b;

}

// Increment function
function increment(n) {
    return n += 1;

}

// Decrement function
function decrement(n) {
    return n -= 1;

}

// makeInt(string)
function makeInt(n) {
    return parseFloat(n);

}
// preserveDecimal(string)
function preserveDecimal(n) {
    return parseFloat(n);

}